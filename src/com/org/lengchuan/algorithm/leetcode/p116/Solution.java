import java.util.LinkedList;
import java.util.Queue;

/**
 * @author lengchuan <lishuijun1992@gmail.com>
 * @date 2020/11/25
 * @desc
 */
public class Solution {
    public Node connect(Node root) {
        // å¹¿æœï¼ŒæŒ‰å±‚æ¬¡éå†ï¼Œåªéœ€è¦è®°å½•ä¸‹ä¸Šä¸€ä¸ªèŠ‚ç‚¹å³å¯ï¼Œå½“éå†åˆ°åŒå±‚ä¸‹ä¸€ä¸ªèŠ‚ç‚¹æ—¶ï¼Œä¸ºä¸Šä¸€ä¸ªèŠ‚ç‚¹è®¾ç½®nextæŒ‡é’ˆ
        if (root == null) {
            return null;
        }

        Queue<Node> queue = new LinkedList<>();
        queue.add(root);
        while (!queue.isEmpty()) {
            int size = queue.size();
            Node last = null;
            while (size > 0) {
                Node node = queue.poll();
                if (last != null) {
                    last.next = node;
                }
                last = node;

                if (node.left != null) {
                    queue.offer(node.left);
                }

                if (node.right != null) {
                    queue.offer(node.right);
                }

                size--;
            }
        }

        return root;
    }

}

class Node {
    public int val;
    public Node left;
    public Node right;
    public Node next;

    public Node() {
    }

    public Node(int _val) {
        val = _val;
    }

    public Node(int _val, Node _left, Node _right, Node _next) {
        val = _val;
        left = _left;
        right = _right;
        next = _next;
    }
}